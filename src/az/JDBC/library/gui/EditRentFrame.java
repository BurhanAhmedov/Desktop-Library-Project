    private void editBookCombo(Book b) {
        try {
            List<Book> bookList = bookService.getBookList();
            DefaultComboBoxModel bookCombo = (DefaultComboBoxModel) bookCmb.getModel();
            for (Book book : bookList) {
                bookCombo.addElement(book);
            }
            bookCombo.setSelectedItem(b);
        } catch (Exception ex) {
            ex.printStackTrace();
        }

    }

    private void editAuthorCombo(Author a) {
        try {
            List<Author> authorList = authorService.getAuthorList();
            DefaultComboBoxModel authorCombo = (DefaultComboBoxModel) authorCmb.getModel();
            for (Author author : authorList) {
                authorCombo.addElement(author);
            }
            authorCombo.setSelectedItem(a);
        } catch (Exception ex) {
            ex.printStackTrace();
        }

    }

    private void editUserCombo(User u) {
        try {
            List<User> userList = userService.getUserList();
            DefaultComboBoxModel userCombo = (DefaultComboBoxModel) userCmb.getModel();
            for (User user : userList) {
                userCombo.addElement(user);
            }
            userCombo.setSelectedItem(u);
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private void editEmployeeCombo(Employee e) {
        try {
            List<Employee> employeeList = employeeService.getEmployeeList();
            DefaultComboBoxModel employeeCombo = (DefaultComboBoxModel) employeeCmb.getModel();
            for (Employee employee : employeeList) {
                employeeCombo.addElement(employee);
            }
            employeeCombo.setSelectedItem(e);
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        try {
            Book book = (Book) bookCmb.getSelectedItem();
            Author author = (Author) authorCmb.getSelectedItem();
            User user = (User) userCmb.getSelectedItem();
            Employee employee = (Employee) employeeCmb.getSelectedItem();
            Rent rent = new Rent();
            rent.setBook(book);
            rent.setAuthor(author);
            rent.setEmployee(employee);
            rent.setUser(user);
            rent.setPayment(Float.valueOf(amountTxt.getText()));
            rent.setLocation(locationTxt.getText());
            rent.setTakeTime(takeTimeTxt.getDate());
            rent.setReturnTime(returnTimeTxt.getDate());
            rentService.editRent(rent);
            JOptionPane.showMessageDialog(null, "Rent is edited!");
            this.dispose();
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error!");
        }

    }//GEN-LAST:event_editBtnActionPerformed

