    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        try {
            String name = nameTxt.getText();
            String surname = surnameTxt.getText();
            String gender = genderTxt.getText();
            Date birthday = birthdayTxt.getDate();
            String position = positionTxt.getText();
            String telephone = telephoneTxt.getText();
            String email = emailTxt.getText();
            String identity = identityTxt.getText();
            String salary = salaryTxt.getText();
            Date recruitment = recruitmentTxt.getDate();
            Date dismissal = dismissalTxt.getDate();
            Employee employee = employeeService.getEmployeeById(selectedId);
            employee.setName(name);
            employee.setSurname(surname);
            employee.setGender(gender);
            employee.setBirthday(birthday);
            employee.setIdendity_No(identity);
            employee.setSalary(Integer.valueOf(salary));
            employee.setTelephone(telephone);
            employee.setE_mail(email);
            employee.setRecruitment(recruitment);
            employee.setDismissal(dismissal);
            employee.setPosition(position);
            employeeService.editEmployee(employee);
            JOptionPane.showMessageDialog(null, "Employee is edited!");
            this.dispose();

        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error!");

        }
    }//GEN-LAST:event_editBtnActionPerformed

