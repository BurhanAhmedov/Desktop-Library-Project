    private void editBookCombo(Book book) {
        try {
            List<Book> bookList = bookService.getBookList();
            DefaultComboBoxModel bookCombo = (DefaultComboBoxModel) bookCmb.getModel();
            for (Book b : bookList) {
                bookCombo.addElement(b);
            }
            bookCombo.setSelectedItem(book);
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private void editGenreCombo(Genre genre) {
        try {
            List<Genre> genreList = genreService.getGenreList();
            DefaultComboBoxModel genreCombo = (DefaultComboBoxModel) genreCmb.getModel();
            for (Genre g : genreList) {
                genreCombo.addElement(g);
            }
            genreCombo.setSelectedItem(genre);
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private void editAuthorCombo(Author author) {
        try {
            List<Author> authorList = authorService.getAuthorList();
            DefaultComboBoxModel authorCombo = (DefaultComboBoxModel) authorCmb.getModel();
            for (Author a : authorList) {
                authorCombo.addElement(a);
            }
            authorCombo.setSelectedItem(author);
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
