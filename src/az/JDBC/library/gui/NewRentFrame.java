    private void showBookCombo() {
        try {
            List<Book> bookList = bookService.getBookList();
            DefaultComboBoxModel bookCombo = (DefaultComboBoxModel) bookCmb.getModel();
            for (Book book : bookList) {
                bookCombo.addElement(book);
            }

        } catch (Exception ex) {
            ex.printStackTrace();
        }

    }

