    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        try {
            Book book = (Book) bookCmb.getSelectedItem();
            Author author = (Author) authorCmb.getSelectedItem();
            User user = (User) userCmb.getSelectedItem();
            Employee employee = (Employee) employeeCmb.getSelectedItem();
            Rent rent = new Rent();
            rent.setBook(book);
            rent.setAuthor(author);
            rent.setEmployee(employee);
            rent.setUser(user);
            rent.setPayment(Float.valueOf(amountTxt.getText()));
            rent.setLocation(locationTxt.getText());
            rent.setTakeTime(takeTimeTxt.getDate());
            rent.setReturnTime(returnTimeTxt.getDate());
            rentService.addRent(rent);
            JOptionPane.showMessageDialog(null, "Rent is created!");
            this.dispose();
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error!");
        }

    }//GEN-LAST:event_saveBtnActionPerformed

