    private void signupBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signupBtnActionPerformed
        LoginDao loginDao = new LoginDaoImpl();
        LoginService loginService = new LoginServiceImpl(loginDao);
        String name = nameTxt.getText();
        String surname = surnameTxt.getText();
        String username = usernameTxt.getText();
        String password = passwordTxt.getText();
        String password2 = password2Txt1.getText();
        try {
            if (password.equals(password2)) {
                loginService.signUp(name, surname, username, password);
                JOptionPane.showMessageDialog(null, "Sign Up is successful!");
                this.dispose();
            } else {
                incorrectTxt.setText("Password is incorrect!");
                incorrectTxt.setVisible(true);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error!");
        }

    }//GEN-LAST:event_signupBtnActionPerformed

